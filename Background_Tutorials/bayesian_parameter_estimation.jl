# Bayesian Inference

## Introduction 

In this tutorial, we provide a brief overview of Bayesian inference. For readers who are interested in acquiring a more thorough understanding of Bayesian inference, there are many valuable resources available. For an accessible book with practical instruction, we recommend Doing Bayesian Data Analysis Kruschke (2014).  We also recommend Kruschke (2011) and Wagenmaker et al. (2018) for a theoretical overview of Bayesian inference. Annis & Palmeri (2018) provide a discussion of Bayesian inference in the context of cognitive models. 

### The intuition

The basic idea behind Bayesian inference is quite simple: it involves updating beliefs as new evidence is acquired. It starts with what is called a prior distribution, which characterizes beliefs about a phenomenon in terms of a probability distribution of difference possibilities. These beliefs are adjusted when new evidence is encountered. For example, your belief about the fairness of a coin will change if you observe 18 heads out of 20 flips. The adjusted belief distribution is called a posterior distribution. The posterior distribution effectively serves as the new prior distribution for evaluating new evidence. Statistician Dennis Lindley encapsulated the iterative process of Bayesian inference with the aphorism: 

> Today’s posterior is tomorrow’s prior 

## Derivation of Bayes's Theorem

The process of updating beliefs is formalized through Bayes theorem, which shows how to relate one conditional probability, $\Pr(A \mid B)$ to its inverse $\Pr(B \mid A)$ through a series of simple algebraic steps. First, we start with two definitions of the joint probability of events $A$ and $B$:

$\Pr(A \wedge B) = \Pr(B \mid A)\Pr(A)$

$\Pr(A \wedge B) = \Pr(A \mid B)\Pr(B)$

In the equations above, $P(A \wedge B)$ is the joint probability of A and B (e.g. the probability that A and B occur together). Note that in both expressions, the right hand side is equal to $\Pr(A \mid B)$. For this reason, the right hand side of both expressions are equal to each other as the following shows:

$\Pr(A \mid B)\Pr(B) = \Pr(B \mid A)\Pr(A)$

In the last step, we can divide both sides by $\Pr(B)$ we can obbtain Bayes's theorem:

$\Pr(A \mid B) = \frac{\Pr(B \mid A)\Pr(A)}{\Pr(B)}$

Alternatively, we could have divided both sides by $\Pr(A)$.

## Base rate neglect

One of the simplest examples of Bayesian inference is described in Gigerenzer & Hoffrage (1995) in the context of base rate neglect---the tendency to underweight the prior probability of hypotheses (or models). The example is a vignette describing a medical test for breast cancer:

> The probability of breast cancer is $1\%$ for a woman at age forty
> who participates in routine screening [base-rate]. If a woman
> has breast cancer, the probability is $80\%$ that she will get a
> positive mammography [hit-rate]. If a woman does not have
> breast cancer, the probability is $9.6\%$ that she will also get a
> positive mammography [false-alarm rate]. A woman in this
> age group had a positive mammography in a routine screening.
> What is the probability that she actually has breast cancer? (pg. 685)

In this example, our task is to pit two hypotheses---the patient has breast cancer or the patient does not have breast cancer---against each other in light of evidence from a positive mammography. For the following example, let $D = \{p,a\}$ represent the state of the disease, which is present (p) or absent (a). Let $M = \{+,-\}$ represent the outcome of the test, which is either positive ($+$) or negative ($-$). According to the provided information, our prior for the hypothesis that the patient has breast cancer, $\Pr(D=+) = .01$. We are given information about the hit rate of the mammogram, which is $\Pr(M=+ \mid D=p) = .80$. In other words, assuming the patient has breast cancer, the mammogram will correctly detect ii with an $80\%$ chance. The other component $\Pr(M=+ \mid D=a) = .096$ gives the probability of a false alarm. What we want to know is $\Pr(D=+ \mid M=+)$ or the probability of breast cancer after taking into account the positive mammogram. The solution is a straight forward application of Baye's theorem.

$\Pr(D=+ \mid M=+) = \frac{\Pr(M=+ \mid D=+)\Pr(D=+)}{\Pr(M=+ \mid D=+)\Pr(D=+) + \Pr(M=p \mid D=-)\Pr(D=-)}$

$\Pr(D=+ \mid M=+) = \frac{.80\cdot .01}{.80\cdot .01 + .096\cdot .99} \approx .077$


At a conceptual level, we computed the evidence for one hypothesis $\Pr(M=+ \mid D=p)$, weighted it by its prior probability $\Pr(D=p)$, performed the same analysis for the other hypothesis, and compared these values as a ratio. Many people consider $7.7\%$ to be a surprising result. The reason the posterior probability is low is because the prior probability is low and the accuracy of the test is high but far from perfect. As a consequence, most people who test positive do not have breast cancer. 

## Parameter Estimation

In the previous example, we used Bayesian inference to compute the posterior probabilities of discrete events. We can use parameter estimation to extend Bayesian inference to situations involving continuous parameters. In simple situations, Bayesian parameter estimation can be performed analytically. We will perform demonstrate how do do so below. 

### Prior Distribution

Suppose we want to estimate the bias of a coin to land on heads, $\theta$. Our first step is define a prior distribution for our bias parameter, $\theta$. We will use a beta distribution to define a prior distribution because it is flexible and bounded to the interval $\theta \in [0,1]$. The beta distribution is defined with two parameters: $\alpha$ and $\beta$. Roughtly speaking, the $\alpha$ parameter can be interpreted as our beliefs in terms of the number of successes and failures for $\beta$. 

Reparameterizing the beta distribution can make it easier to intepret. A more convenient parameterization is $n_p = \alpha + \beta$ and $\mu_{\theta} = \frac{\alpha}{\alpha + \beta}$. The interactive plot below allows you to explore how the parameters $n_p$ and $\mu_{\theta}$ change the shape of the prior distribution of $\theta$. The default setting of $n_p = 2$ and $\mu_{\theta} = .5$ creates a uniform distribution in which each value of $\theta$ has the same density, i.e. they are equally likely. Increasing $n_p$ will put more density near values of .5, indicating greater confidence. Changing $\mu_{\theta}$ will shift where the density is concentrated. Overall, the beta distribution is quite flexible in the prior beliefs it allows you to express. 

"

