## Likelihood Function

The likelihood function computes the probability of the data $\mathbf{Y} = \{y_1,y_2,\dots, y_n\}$ under the model given some set of parameters $\Theta = \{\theta_1,\theta_2,\dots,\theta_m\}$. In a likelihood function, the parameters can vary, but the data are fixed. A general representation of the likelihood function is given by 

$\mathcal{L}(\Theta; \mathbf{Y}) = \Pi_i^Nf(y_i \mid \Theta)$. 

In our particular case, the likelihood function for a model of coin flips is given by the Binomial likelihood:

$\mathcal{L}(\theta; h, n) = \Pr(H=h) = {n\choose h} \theta^h(1-\theta)^{n-h}$

where $h$ is the number of observes heads out of $n$ trials, $\theta$ is probability of heads, and ${n\choose h}$ is the binomial coefficient which represents the number of ways in which $h$ heads and $n-h$ tails can be obtained. 
"

