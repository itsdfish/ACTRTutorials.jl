The mean of the posterior distribution is $\mathop{\mathbb{E}}[\theta \mid  h,n] = \frac{1 + 7}{1 + 1 + 10}$. The same value in Julia is computed as:
"

# ╔═╡ 7efc7c8c-b7f9-4882-9420-de6af1ce9d75
