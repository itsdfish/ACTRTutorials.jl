## Posterior Distribution

The posterior distribution of $\theta$ represents our beliefs about possible values of $\theta$ after taking into account the new data. One way to think about it is as a compromise between our prior beliefs and the data. On one hand, the prior distribution indicates weak belief that the coin is unbiased. On the other hand, the data provide weak evidence that the coin is biased towards heads. As a compromise, the posterior distribution falls between the prior and the data. 

The Beta prior and binomial likelihood are conjugate, meaning the posterior distribution is also a Beta distribution. The parameters for the posterior distribution are $\textrm{Beta}(h+\alpha, n-h+\beta)$. In other words, we add the number of heads to the value $\alpha$ used in the prior and the number of tails to the value $\beta$ used in the prior. 

The mean posterior probability for $\theta$ is expressed as follows:

$\frac{\alpha+h}{\alpha + \beta + n}$

As an example, suppose we observe $h=7$ heads out of $n=10$ trials. Suppose further that the prior of $\theta$ is uniform: $\theta \sim \textrm{Beta}(1,1)$. The mean of the prior is $\mathop{\mathbb{E}}[\theta] = \frac{1}{1 + 1}$. This is computed as follows with code:
"

# ╔═╡ 906a68d6-5df7-45f3-84c0-4320a319ea25
mean(Beta(1, 1))

