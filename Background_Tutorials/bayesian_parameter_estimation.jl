# Posterior Predictive Distribution

A posterior predictive distribution represents uncertainty predictions of future data, taking into consideration uncertainty in the paramter estimates. This is found by computing the product of the likelihood of data and posterior probability of the parameters, and integrating over the parameters. In the present case, the posterior predictive distribution has a known distributional form called a Beta-Binomial distribution:

$f(k \mid n,\alpha, \beta) = \int_0^1 \mathcal{L}(\theta; k)\pi(\theta \mid \alpha,\beta)d\theta$
"

