### Conflict resolution and perceptual-motor time

Other processes in ACT-R contribute to the observed reaction time, including conflict resolution and perceptual-motor processes. A common simplification in applications of ACT-R involves fixing the processing time for each of these processes to the theoretically based expected value.  This is also a common practice in a large class models called sequential sampling models. This residual time is a contsant usually denoted as $t_{\textrm{er}}$. $t_{\textrm{er}}$ is typically treated as a parameters that is estimated from the data. One advantage of ACT-R, however, is that $t_{\textrm{er}}$ can be derived from the architecture. This model has three productions: encode, retrieve, and respond, each with a conflict resolution. Thus, we will fix this parameter at 

$\begin{align}
 t_\textrm{er} = \rm (cr + e) + cr +  (cr + me) = .295
\end{align}$

where cr is the default conflict resolution time of .05 seconds, e is the default encoding time of .085 seconds and me is the default motor execution time of .06 seconds. 

### Likelihood Function

We define $\mathbf{Y} = \{y_1,y_2,\dots y_{n}\}$ as a set of reaction times for $n$ observations. The likelihood function is defined as:

$\begin{align}
    \mathcal{L}(\textrm{blc};\mathbf{Y}, s, \mathbf{r}_i) = \prod_{i=1}^{n} f_{\rm Lognormal}(y_i-t_{\rm er}| -\textrm{blc}, \sigma)
\end{align}$

where $f_{\rm Lognormal}$ is the PDF of the Lognormal distribution, which is defined as

$\begin{align}
f_{\rm Lognormal}(t; \mu, \sigma) = \frac{1}{t\sigma \sqrt{2\pi}}e^{- \frac{\left(log(t)-\mu\right)^2}{2\sigma^2} } 
\end{align}$

The following summarizes the assumption of the model:


1. Retrieval failures are negligible
2. Activation is identical and constant for each chunk except for noise
3. Each data point is independent of others
4. Reaction times follow a Lognormal distribution with a constant offset 
"

