## Task

As with the tasks described in Unit 1, we will use variations of a simple paired associates task throughout Unit 2. In the paired associates task, subjects learn arbitrary letter number pairs, such as

- (X, 2)
- (M, 5)

In the test phase, a letter is presented and the subject must recall the associated number and press the corresponding key on the keyboard. In this particular version of the task, subjects can respond "I don't know" if they are unable to remember the number. 

# Simple RT Model 1

The model is based on the assumption that memories are highly distinguishable and have identical and stable activation, with the exception of transient noise. The model also assumes that the correct answer is always retrieved. What differs from trial to trial is the time required to retrieve and respond. 

## Declarative memory

Let $M$ denote the set of $n$ chunks in declarative memory, one chunk for each stimulus in the experiment. Each chunk consists of the following set of slots: $Q= \rm \{letter,number\}$. 

### Encoding

On each trial $i$, a letter $v$ is presented on a screen and the model encodes the stimulus into a chunk that is stored in the imaginal buffer. Formally, we define the encoded chunk as:

$\begin{align}
\mathbf{c}_{i,\rm \textrm{imaginal}} = \{ (\rm letter, v)\}
\end{align}$

### Retrieval Request

The retrieval request on trial $i$ is defined as:

$\begin{align}
\mathbf{r}_i = \{(\textrm{cue},c_{i,\rm imaginal}(\rm letter)\} 
\end{align}$

where $c_{i,\rm imaginal}(\rm letter)$ is the letter  of the chunk in the imaginal buffer. The slot set for the retrieval request is defined as $Q_r = \rm \{letter\}$ 

### Activation

The Simple RT Model 1 is similar to the Simple Choice Model from the previous unit. As before, the activation equation is defined as:

$\begin{align}
a_m = \rm blc + \epsilon_m
\end{align}$

where blc is the base level constant and $\epsilon_m \sim \rm normal(0,\sigma)$.
### Reaction Time

In ACT-R, reaction time is the sum of many cognitive processes, which are broadly categorized as retrieval time and perceptual-motor time. In what follows, we will specify the processing times that comprise the Simple RT Model 1. 

#### Retrieval Time

In ACT-R, retrieval time is a monotonically decreasing function of activation: as activation increases, reaction time decreases. In particular, retrieval time is related to the following distribution:


$\begin{align}
t_m \sim e^{-a_m}
\end{align}$


The resulting distribution is called a Lognormal distribution. A random variable follows a normal distribution when it is normally distributed following a logarithmic transformation. Formally, the distribution of $t_m$ is stated as:

$\begin{align}
t_m \sim \rm lognormal(-\mu_m,\sigma) 
\end{align}$
"""

