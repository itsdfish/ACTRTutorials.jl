# Generate Data

We will use the function below to generate data for parameter estimation. The function `simulate` accepts the following arguments:
- parms: a `NamedTuple` of fixed parameters
- blc: a constant value added to activation. The annotated code below shows how an `ACTR` object is created, a chunk is retrieved and a reaction time is computed.
