# ╔═╡ af537005-cf28-40ab-a06d-b54702a4b8dd
md"

As expected, the mean of the posterior distribution is situated between the mean of the prior distribution and the mean of the data. In fact, we can rewrite the expression for the posterior mean in terms of a weighted average of the prior and the data:

$\frac{\alpha+h}{\alpha + \beta + n} = \frac{h}{n}\frac{n}{n+\alpha+\beta} + \frac{\alpha}{\alpha+\beta}\frac{\alpha+\beta}{n+\alpha+\beta}$

Let's break down the right hand side. Starting on the left, 

-  $\frac{h}{n}$ is the mean of the data

-  $\frac{n}{n+\alpha+\beta}$ is the weight of the data

-  $\frac{\alpha}{\alpha+\beta}$ is the mean of the prior distribution

-  $\frac{\alpha+\beta}{n+\alpha+\beta}$ is the weight of the prior distribution

"

